# 4. ALGORITMO DE ORDENAÇÃO (Selection Sort)
# ============================================================

def ordenar_fila_por_prioridade(modulos, prioridades):
    """
    Ordena a fila de pouso pela prioridade (1 = mais alta).
    Implementação didática do Selection Sort.
    Complexidade: O(n²).
    """
    # Combina prioridades e nomes em uma lista de tuplas
    fila = list(zip(prioridades, modulos))

    # Selection sort: a cada passo, encontra o menor elemento
    # restante e o coloca na posição i
    for i in range(len(fila)):
        menor = i
        for j in range(i + 1, len(fila)):
            if fila[j][0] < fila[menor][0]:
                menor = j
        # Troca o elemento da posição i com o menor encontrado
        fila[i], fila[menor] = fila[menor], fila[i]

    # Extrai apenas os nomes dos módulos, já ordenados
    fila_ordenada = []
    for _prioridade, modulo in fila:
        fila_ordenada.append(modulo)

    return fila_ordenada


