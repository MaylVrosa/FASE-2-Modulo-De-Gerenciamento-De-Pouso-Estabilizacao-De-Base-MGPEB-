def menor_combustivel(niveis):
    """
    Busca linear pelo menor nível de combustível.
    Retorna uma tupla (valor_minimo, indice_do_modulo).
    Complexidade: O(n).
    """
    menor = niveis[0]
    index_menor = 0

    for i, combustivel in enumerate(niveis):
        if combustivel < menor:
            menor = combustivel
            index_menor = i

    return menor, index_menor


def maior_prioridade(prioridades):
    """
    Busca linear pelo módulo de maior prioridade.
    Como prioridade 1 é a mais alta, procura-se o MENOR número.
    Retorna (valor_da_prioridade, indice_do_modulo).
    """
    maior = prioridades[0]
    index_maior = 0

    for i, p in enumerate(prioridades):
        if p < maior:   # menor número = maior prioridade
            maior = p
            index_maior = i

    return maior, index_maior


def buscar_tipo_carga(tipos_carga, tipo):
    """
    Busca linear por um tipo específico de carga.
    Retorna o índice do módulo, ou -1 se não encontrado.
    """
    for i, carga in enumerate(tipos_carga):
        if carga == tipo:
            return i
    return -1


