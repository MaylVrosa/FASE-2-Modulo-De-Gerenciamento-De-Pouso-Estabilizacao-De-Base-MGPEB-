# ---- 2.1 FILA (FIFO — First In, First Out) ------------------
class Fila:
    """
    Fila de pouso dos módulos.
    Disciplina FIFO: o primeiro módulo a entrar é o primeiro
    a ser avaliado para pouso. Implementada com list para
    fins didáticos (em produção, recomenda-se collections.deque
    pela eficiência O(1) na remoção).
    """

    def __init__(self):
        self.itens = []

    def enqueue(self, item):
        """Insere um item no final da fila."""
        self.itens.append(item)

    def dequeue(self):
        """Remove e retorna o primeiro item da fila (FIFO)."""
        if not self.is_empty():
            return self.itens.pop(0)
        return None

    def is_empty(self):
        """Retorna True se a fila estiver vazia."""
        return len(self.itens) == 0

    def size(self):
        """Retorna a quantidade de itens na fila."""
        return len(self.itens)


# ---- 2.2 LISTA ENCADEADA -----------------------------------
class Node:
    """
    Nó de uma lista encadeada simples.
    Cada nó guarda um módulo, seu horário de chegada
    e uma referência para o próximo nó.
    """

    def __init__(self, modulo, horario_chegada_orbita):
        self.modulo = modulo
        self.horario_chegada_orbita = horario_chegada_orbita
        self.next = None


def push_front(head, modulo, horario_chegada_orbita):
    """
    Insere um novo nó no início da lista encadeada.
    Retorna a nova cabeça da lista.
    """
    novo_no = Node(modulo, horario_chegada_orbita)
    novo_no.next = head
    return novo_no


def print_list(head):
    """Percorre a lista encadeada e imprime os módulos em sequência."""
    atual = head
    while atual is not None:
        print(atual.modulo, end=" -> ")
        atual = atual.next
    print("END")


# ---- 2.3 PILHA (LIFO — Last In, First Out) -----------------
class Pilha:
    """
    Pilha de módulos / decisões.
    Disciplina LIFO: o último item inserido é o primeiro a sair.
    Útil para registrar histórico de decisões, onde a mais
    recente é a mais relevante para consulta imediata.
    """

    def __init__(self):
        self.itens = []

    def push(self, item):
        """Empilha um item no topo."""
        self.itens.append(item)

    def pop(self):
        """Desempilha e retorna o item do topo (LIFO)."""
        if self.itens:
            return self.itens.pop()
        return None

    def peek(self):
        """Consulta o topo da pilha sem remover."""
        if self.itens:
            return self.itens[-1]
        return None


